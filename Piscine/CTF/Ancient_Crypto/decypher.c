#include <unistd.h>

int	main(void)
{
	int	i;
	int	j;
	char c;
	char	*s;
	
	i = 0;
	s = "OKCI_DY_LBEDO_PYBMO";
	while (i <= 255)
	{
		j = 0;
		while (s[j])
		{
			c = s[j] + i;
			write(1, &c, 1);
			j++;
		}
		i++;
		write (1, "\n", 1);
	}
}
