#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int i)
{
	char	c;

	if (i == -2147483648)
	{
		write(1,"-2147483648", 11);
	}
	else
	{
		if (i < 0)
		{
			ft_putchar('-');
			i = -i;
		}
		c = '0' + (i % 10);
		if (i < 10)
		{
			ft_putchar(c);
		}
		else
		{
			ft_putnbr(i / 10);
			ft_putchar(c);
		}
	}
}
