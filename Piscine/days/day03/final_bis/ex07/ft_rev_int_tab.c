void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *b;
	*b = *a;
	*a = tmp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	n;

	n = 0;
	while (n < size / 2)
	{
		ft_swap(tab + n, tab + (size - n -1));
		n++;
	}
}
