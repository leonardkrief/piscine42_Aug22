char	*ft_strupcase(char *str)
{	
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] > 'a' - 1) && (str[i] < 'z' + 1))
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
